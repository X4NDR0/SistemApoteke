namespace ConsoleApp1.Models
{
    /// <summary>
    /// Class used for the model representing the procurement of medicine.
    /// </summary>
    public class NabavkaLeka
    {
        /// <summary>
        /// Property representing the name of the procurement medicine.
        /// </summary>
        public string Naziv;

        /// <summary>
        /// Property representing the price of the procurement medicine.
        /// </summary>
        public double Cena;

        /// <summary>
        /// Property representing the total price of the procurement medicine.
        /// </summary>
        public double UkupnaCena;

        /// <summary>
        /// Property representing the quantity of the procurement medicine.
        /// </summary>
        public int Kolicina;
    }
}
