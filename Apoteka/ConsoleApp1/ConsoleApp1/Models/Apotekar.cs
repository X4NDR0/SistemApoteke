namespace Zadatak2SistemApoteke
{
    /// <summary>
    /// Class used for the model representing the pharmacist.
    /// </summary>
    public class Apotekar
    {
        /// <summary>
        /// Property representing an unique id of the pharmacist
        /// </summary>
        public int IdentifikacioniBroj;

        /// <summary>
        /// Property representing the name of the pharmacist.
        /// </summary>
        public string Ime;

        /// <summary>
        /// Property representing the last name of the pharmacist.
        /// </summary>
        public string Prezime;

        /// <summary>
        /// Property representing the phone number of the pharmacist.
        /// </summary>
        public int BrojTelefona;
    }
}
